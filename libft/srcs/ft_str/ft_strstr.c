#include "libft.h"

char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	j;

	j = 0;
	i = 0;
	if (*to_find == 0)
		return (str);
	if (ft_strlen(&str[i]) < ft_strlen(&to_find[i]))
	{			
		return (0);
	}
	while (str[i] != '\0')
	{
        j = 0;
		while (to_find[j] == str[i + j])
		{
			if (to_find[j + 1] == '\0')
			{
				return (&str[i]);
			}
			j++;
		}
		i++;
	}
	return (0);
}